# non parameterized function
"""
Syntax :
def function_name():
    statement

function_name()     <= Calling the Function
"""
def nonparam():
    print('I am Non Parameterized Function')

nonparam()


# parameterized function
"""
Syntax :
def function_name(param1,param2,param3,...):
    statement

function_name()     <= Calling the Function
"""
def param(a,b):
    print('I am Parameterized Function',a,b)

param("Hello","World")



